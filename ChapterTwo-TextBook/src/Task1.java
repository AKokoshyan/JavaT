
public class Task1 {
	/**
	 * To be made a program which is printing the squares of the numbers from 1
	 * to 5!
	 */

	public static void main(String[] args) {
		// Below initialize the variables!
		byte firstNumber = 1;
		byte secondNumber = 2;
		byte thirdNumber = 3;
		byte fourthNumber = 4;
		byte fifthNumber = 5;

		// Below declare and initialize a variables with the square of each
		// number!
		int firstNumPowTwo = firstNumber * firstNumber;
		int secondNumPowTwo = secondNumber * secondNumber;
		int thirdNumPowTwo = thirdNumber * thirdNumber;
		int fourthNumPowTwo = fourthNumber * fourthNumber;
		int fifthNumPowTwo = fifthNumber * fifthNumber;

		// Here printing the squares of all numbers!
		System.out.print(firstNumPowTwo + ", " + secondNumPowTwo + ", " + thirdNumPowTwo + ", " + fourthNumPowTwo + ", "
				+ fifthNumPowTwo);

	}

}
